<?php

class page_reports_loan_noc extends Page {
	public $title="NOC Repots";
	function page_index(){
		// parent::init();

		$till_date="";
		
		if($_GET['to_date']){
			$till_date=$_GET['to_date'];
		}

		$form=$this->add('Form');
		$form->addField('autocomplete/Basic','account_number');

		$form->addSubmit('GET List');

